import { existsSync, realpathSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDirectory = dirname(fileURLToPath(import.meta.url));

const resolveCanonicalPackageRoot = () => {
  const candidates = [
    join(scriptDirectory, 'node_modules/eslint-config-canonical'),
    join(scriptDirectory, '../node_modules/eslint-config-canonical'),
  ];

  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return realpathSync(candidate);
    }
  }

  throw new Error(
    'eslint-config-canonical is not linked. Run `npm run setup-dev` first.',
  );
};

const requireFromCanonical = createRequire(
  join(resolveCanonicalPackageRoot(), 'package.json'),
);
const typescriptEslintPlugin = requireFromCanonical(
  '@typescript-eslint/eslint-plugin',
);

const outputPath = resolve(
  scriptDirectory,
  '../configurations/typescript-compatibility.ts',
);

const loadCanonicalRules = async () => {
  try {
    const { recommended } = await import('eslint-config-canonical/canonical');

    return recommended?.[0]?.rules ?? {};
  } catch (error) {
    throw new Error(
      'Could not load eslint-config-canonical/canonical. Run `npm run setup-dev` and `npm run build` first.',
      { cause: error },
    );
  }
};

const serializeRuleValue = (ruleValue, indent) => {
  return JSON.stringify(ruleValue, null, 2)
    .split('\n')
    .map((line, index) => {
      return index === 0 ? line : indent + line;
    })
    .join('\n');
};

const serializeRules = (rules) => {
  const entries = Object.entries(rules).toSorted(([left], [right]) => {
    return left.localeCompare(right);
  });

  return entries
    .map(([ruleName, ruleValue]) => {
      return `    ${JSON.stringify(ruleName)}: ${serializeRuleValue(ruleValue, '    ')},`;
    })
    .join('\n');
};

/**
 * Overrides that are not derived from `@typescript-eslint` `extendsBaseRule`.
 * Kept here so regenerating the file does not drop intentional TS-scoped tweaks.
 */
const manualOverrides = {
  'react/prop-types': 0,
};

const canonicalRules = await loadCanonicalRules();
const appendRules = {};

for (const [ruleName, rule] of Object.entries(typescriptEslintPlugin.rules)) {
  const extendsBaseRule = rule.meta?.docs?.extendsBaseRule;

  if (!extendsBaseRule) {
    continue;
  }

  const baseRuleName = extendsBaseRule === true ? ruleName : extendsBaseRule;

  if (canonicalRules[baseRuleName] === undefined) {
    continue;
  }

  appendRules[baseRuleName] = 0;
  appendRules['@typescript-eslint/' + ruleName] = canonicalRules[baseRuleName];
}

Object.assign(appendRules, manualOverrides);

const source = `import { defineConfig } from 'eslint/config';

/**
 * Auto-generated by \`npm run generate-typescript-compatibility-rules\`.
 * Do not edit this file manually.
 * Disables core ESLint rules that are superseded by @typescript-eslint extensions
 * and mirrors their configuration onto the TypeScript equivalents.
 * Also applies manual TS-scoped overrides (see script \`manualOverrides\`).
 */
export const recommended = defineConfig({
  files: ['**/*.{ts,tsx}'],
  rules: {
${serializeRules(appendRules)}
  },
});
`;

writeFileSync(outputPath, source);

console.log(`Wrote ${outputPath}`);
